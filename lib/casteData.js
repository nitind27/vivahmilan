// ─── HELPER ───────────────────────────────────────────────────────────────────
function toOptions(obj) {
  const result = [];
  for (const [group, items] of Object.entries(obj)) {
    items.forEach(c => result.push({ val: c, label: c, group }));
  }
  return result;
}

export function getCastesByReligion(religion) {
  switch (religion) {
    case 'Hindu':     return toOptions(HINDU_CASTES);
    case 'Muslim':    return toOptions(MUSLIM_COMMUNITIES);
    case 'Christian': return toOptions(CHRISTIAN_DENOMINATIONS);
    case 'Sikh':      return toOptions(SIKH_CASTES);
    case 'Jain':      return toOptions(JAIN_CASTES);
    case 'Buddhist':  return toOptions(BUDDHIST_CASTES);
    case 'Parsi':     return toOptions(PARSI_CASTES);
    case 'Jewish':    return toOptions(JEWISH_CASTES);
    default:          return [{ val: "Doesn't Matter", label: "Doesn't Matter", group: 'Other' }];
  }
}
